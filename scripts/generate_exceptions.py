#!/usr/bin/env python3
"""
Exception Generator for BambooHR Python SDK.

Generates the specific exception classes in bamboohr_sdk/exceptions.py
from the ERROR_MESSAGES catalog defined in bamboohr_sdk/api_error_helper.py.

Usage:
    python scripts/generate_exceptions.py

Reference: PHP SDK scripts/generate_exceptions.php
"""

from __future__ import annotations

import sys
import textwrap
from pathlib import Path

# ---------------------------------------------------------------------------
# Ensure the SDK package is importable
# ---------------------------------------------------------------------------
PROJECT_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from bamboohr_sdk.api_error_helper import ERROR_MESSAGES  # noqa: E402

EXCEPTIONS_FILE = PROJECT_ROOT / "bamboohr_sdk" / "exceptions.py"

# Markers that delimit the auto-generated section in exceptions.py
BEGIN_MARKER = (
    "# NOTE: This section is auto-generated by scripts/generate_exceptions.py."
)
END_MARKER = (
    "# ---------------------------------------------------------------------------\n"
    "# Backward-compatible aliases"
)

# Template for a single exception class
_TRIPLE = '"""'
CLASS_TEMPLATE = (
    'class {class_name}({parent_class}):\n'
    '    {triple}Exception for HTTP {status_code} {title}.{triple}\n'
    '\n'
    '    @staticmethod\n'
    '    def potential_causes() -> list[str]:\n'
    '        return [\n'
    '{causes}\n'
    '        ]\n'
    '\n'
    '    @staticmethod\n'
    '    def debugging_tips() -> list[str]:\n'
    '        return [\n'
    '{tips}\n'
    '        ]\n'
    '\n'
)


def _format_list_items(items: list[str], indent: str = "            ") -> str:
    """Format a list of strings as indented Python string literals."""
    lines = [f'{indent}"{item}",' for item in items]
    return "\n".join(lines)


def _determine_parent(status_code: int) -> str:
    """Return 'ClientException' for 4xx, 'ServerException' for 5xx+."""
    return "ClientException" if status_code < 500 else "ServerException"


def generate_exception_classes() -> str:
    """Generate the Python source for all specific exception classes."""
    client_classes: list[str] = []
    server_classes: list[str] = []

    for status_code in sorted(ERROR_MESSAGES.keys()):
        info = ERROR_MESSAGES[status_code]
        type_name = info["type"]
        class_name = f"{type_name}Exception"
        parent = _determine_parent(status_code)
        title = info["title"].title()

        block = CLASS_TEMPLATE.format(
            class_name=class_name,
            parent_class=parent,
            status_code=status_code,
            title=title,
            triple=_TRIPLE,
            causes=_format_list_items(info.get("causes", [])),
            tips=_format_list_items(info.get("tips", [])),
        )

        if status_code < 500:
            client_classes.append(block)
        else:
            server_classes.append(block)

    # Build the combined section
    parts = []

    # Client exceptions header
    parts.append(
        "# ---------------------------------------------------------------------------\n"
        "# Specific client exceptions (4xx)\n"
        "# ---------------------------------------------------------------------------\n"
        "# NOTE: This section is auto-generated by scripts/generate_exceptions.py.\n"
        "#       Do not edit manually — run the script to regenerate.\n"
        "# ---------------------------------------------------------------------------\n"
    )
    for block in client_classes:
        parts.append(block)

    # Server exceptions header
    parts.append(
        "# ---------------------------------------------------------------------------\n"
        "# Specific server exceptions (5xx+)\n"
        "# ---------------------------------------------------------------------------\n"
    )
    for block in server_classes:
        parts.append(block)

    return "\n".join(parts)


def _build_status_mapping_block() -> str:
    """Generate the _STATUS_TO_EXCEPTION_CLASS dict source."""
    lines = [
        "# ---------------------------------------------------------------------------",
        "# Status code → exception class mapping (used by ApiException.from_response)",
        "# ---------------------------------------------------------------------------",
        "",
        "_STATUS_TO_EXCEPTION_CLASS: dict[int, type[ApiException]] = {",
    ]
    for status_code in sorted(ERROR_MESSAGES.keys()):
        info = ERROR_MESSAGES[status_code]
        class_name = f"{info['type']}Exception"
        lines.append(f"    {status_code}: {class_name},")
    lines.append("}")
    return "\n".join(lines)


def main() -> None:
    source = EXCEPTIONS_FILE.read_text()

    # Locate the auto-generated section
    begin_idx = source.find(BEGIN_MARKER)
    end_idx = source.find(END_MARKER)

    if begin_idx == -1 or end_idx == -1:
        print(
            "ERROR: Could not find auto-generation markers in exceptions.py.\n"
            f"  Looking for BEGIN: {BEGIN_MARKER!r}\n"
            f"  Looking for END:   {END_MARKER!r}",
            file=sys.stderr,
        )
        sys.exit(1)

    # Find the start of the line containing BEGIN_MARKER
    # (go back to the "# Specific client exceptions" header block)
    section_start = source.rfind(
        "# ---------------------------------------------------------------------------\n"
        "# Specific client exceptions",
        0,
        begin_idx,
    )
    if section_start == -1:
        section_start = begin_idx

    # The END_MARKER starts the backward-compatible aliases block — keep it
    section_end = end_idx

    # Generate new content
    generated = generate_exception_classes()

    # Also regenerate the _STATUS_TO_EXCEPTION_CLASS mapping
    mapping_marker = "# Status code → exception class mapping"
    mapping_start = source.find(mapping_marker)

    if mapping_start == -1:
        print("ERROR: Could not find _STATUS_TO_EXCEPTION_CLASS marker.", file=sys.stderr)
        sys.exit(1)

    # Find the line with the opening "# ---" before the mapping marker
    mapping_section_start = source.rfind("# ----", 0, mapping_start)
    if mapping_section_start == -1:
        mapping_section_start = mapping_start

    # Find the closing "}" of the dict
    mapping_dict_end = source.find("}", mapping_start)
    if mapping_dict_end == -1:
        print("ERROR: Could not find end of _STATUS_TO_EXCEPTION_CLASS dict.", file=sys.stderr)
        sys.exit(1)
    mapping_section_end = mapping_dict_end + 1  # include the "}"

    # Build the new source
    new_source = (
        source[:section_start]
        + generated
        + "\n"
        + source[section_end:mapping_section_start]
        + _build_status_mapping_block()
        + "\n"
        + source[mapping_section_end:]
    )

    EXCEPTIONS_FILE.write_text(new_source)

    # Count generated classes
    count = len(ERROR_MESSAGES)
    print(f"Generated {count} exception classes in {EXCEPTIONS_FILE}")
    print("  Client exceptions (4xx): "
          f"{sum(1 for c in ERROR_MESSAGES if c < 500)}")
    print("  Server exceptions (5xx+): "
          f"{sum(1 for c in ERROR_MESSAGES if c >= 500)}")


if __name__ == "__main__":
    main()
